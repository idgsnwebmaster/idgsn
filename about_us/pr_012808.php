					<h3>IDG Communications Research Finds Wide Gap Between Technology Vendors
					Marketing and IT Buyers&rsquo; Information Needs</h3>
					<h4>            Research also Details What Buyers Value and When During
					Purchase Process</h4>
					<p> FRAMINGHAM, MA&mdash;January 28, 2008&mdash;IDG Communications,
					the leading IT media and events company in the US, has completed
					groundbreaking research into the information needs of corporate technology
					buyers as they move from consideration to purchase of products and
					services. The survey found that
					prospects spend approximately 24 days a year collecting information
					and that IT vendors are not making the best use of the limited time
					they have to sell to a prospect.</p>
					<p>The online research conducted by
					IDC last summer, IDG&rsquo;s renowned research
					company, involved 333 IT professionals and line of business purchasers
					across a range of industries and company sizes. The respondents
					said they rely on content that is relevant and credible based on
					third-party reports and peer-based material such as white papers
					and customer case studies. They
					were not as concerned as IT vendors are with the appearance of the
					information in print, online, or at events.</p>
					<h4>IDG Market Fusion Services Change the Buyer/Seller Relationship</h4>
					<p>          IDG is changing the way sales leads are identified, cultivated,
					and qualified with the introduction of <a href="http://www.idgmarketfusion.com/" target="_blank">IDG
					Market Fusion</a> services (www.idgmarketfusion.com). The research
					by IDC features an in-depth analysis of how IT purchasing decisions
					are made, identifies the buying committees, and provides a roadmap
					for what marketing content is best at various stages of the buying
					cycle. With this understanding
					of buyer expectations, IDG Market Fusion services target the right
					audience with appropriate information, analyzes their engagement
					behavior with a mix of vendor and independent content, and delivers
					additional content at the right time to accelerate the purchase process.</p>
					<p>In
					addition to the buyer research, customer intelligence (CI) tools
					are used to track content consumption in IDG media Web sites (computerworld.com,
					infoworld.com, networkworld.com, cio.com, and idgconnect.com). With
					those tools, IDG can see what content is read by whom and at when.
					This behavioral profiling enables IDG to score the quality of the
					lead and transfer it to a vendor customer when the prospect has the
					greatest potential to turn into new business. A
					new, custom Engagement Intensity Index&reg; (EI2) algorithm analyzes
					prospect interactions with content type and sophistication, the timeframe
					within which content was consumed, and the pathways that led to and
					from various informational assets.  With that information,
					IDG Market Fusion services build a custom  &ldquo;curriculum&rdquo; that
					will lead the prospect through a sequence of content interactions
					that result in a lead of highly actionable quality.</p>
					<p>&ldquo;The research challenges the traditional marketer assumptions
					about the best ways to reach qualified prospects,&rdquo; said Matthew
					Yorke, senior vice president, sales and marketing, IDG Communications. &ldquo;Despite
					widespread agreement that customer focus is growing in importance,
					the research confirms that the priorities of IT sales and marketing
					and the priorities of buyers are misaligned. When sellers are
					in better alignment with their prospects and have the right content
					at the right time, the vendors can significantly increase their chances
					of gaining new business.&rdquo;</p>
					<h4>Highlights of the IDG Market
					Fusion Research</h4>
					<p>The research focused on four steps from consideration to purchase:</p>
					<ul>
					<li><strong>General Education</strong><br />
					Buyers are most interested in reading
					credible, third-party information such as technical or market overviews.</li>
					<li><strong>Business
					Case Development</strong><br />
					Overview material by independent
					authorities is again the most preferred source followed by information
					on a specific product including functionality and fit.</li>
					<li><strong>Vendor
					Shortlist and Evaluations</strong><br />
					At this stage, technology
					enabled business solutions and cost considerations including ROI
					and total cost of ownership are in demand.</li>
					<li><strong> Final Selection</strong><br />
					Price and terms and conditions
					rise to the top at this stage. </li>
					</ul>
					<p>In addition, technology related media is relied upon more than any
					other information including peers throughout the purchase cycle. Buyers
					content and format preferences vary from print, online, and search
					in the awareness phase to white papers, webcasts or podcasts during
					consideration, followed by analytical tools and face-to-face meetings
					as a vendor is selected.</p>
					<h4>Research Methodology</h4>
					<p>            The IDC Enterprise Panel represents IT and line of business professionals
					from US and international companies drawn from IDG media sites
					such as Computerworld, Network World, InfoWorld, and CIO. In
					addition, IDG Connect provides professionals from its database
					of several million readers and show attendees. The web-based
					IDG Market Fusion survey last June attracted 333 respondents representing
					a cross-section of titles, industries, and company sizes. The
					participants handled on average 15 IT contracts in 12 months.</p>
