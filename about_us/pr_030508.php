          <p>&ldquo;The Web allows a lot of talented writers to build a large following of important buying audiences. Yet, in many cases these independent publishers do not have enough resources to generate the true revenue potential of their brands,&rdquo; said Kevin Normandeau, VP, Sales and Business Development, IDG TechNetwork based in Framingham, MA. &ldquo;IDG has the publishing resources, market knowledge and credibility with advertisers to help these publishers grow their revenues and build their businesses.&rdquo;</p>
