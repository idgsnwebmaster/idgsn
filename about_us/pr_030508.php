          <h3>IDG TechNetwork Debuts Media Network for Marketers to Reach Audiences of Independent Technology Publishers</h3>
          <p><strong>FRAMINGHAM, MA &ndash; March 5, 2008</strong> &ndash; Independent publishers looking to grow revenues and increase traffic but with limited resources now have an established business partner: IDG, the world&rsquo;s largest technology media company. The IDG TechNetwork represents publishers to well-known technology marketers who trust IDG to place advertisements in appropriate sites. In return, publishers will receive a share of the revenue and gain valuable member benefits to help them grow their businesses.</p>
